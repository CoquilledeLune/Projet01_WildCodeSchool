    <h2>Histoire</h2>
    <p>
    En 1512, Louis XII ordonne la construction de remparts pour protéger la ville de Lyon. Ces derniers, prenaient naissance à la rupture des pentes et isolaient le plateau sur sa partie sud, créant ainsi un nouveau faubourg aux abords de Lyon. C’est vers la moitié du XVIème siècle que ce faubourg pris le nom qu’on lui connaît aujourd’hui. En 1560, une croix en pierre de Couzon, caractérisée par sa couleur ocre fut installée sur le plateau de la colline. C’est elle qui donna son nom au jeune quartier.
    </p>
    <!-- image fortification croix-rousse + croix rousse originel -->
    <p>
    Aux XVIIème et XVIIIème siècles, la Croix-Rousse se développe. L’élevage et la culture des légumes sont les principales activités faisant vivre le faubourg. Au fil du temps, le négoce prend de l’ampleur et le quartier attire peu à peu des artisans et des commerçants. Au XVIIIème siècle, ces derniers plébiscitent la Croix-Rousse, en raison des taxes en vigueur moins élevées qu’à Lyon. Des communautés religieuses prennent également possession de nombreux terrains et s’établissent dans les hauteurs.
    </p>
    <!-- image croix-rousse gravure -->
    <p>
    Au cours du XIXème siècle, de hauts immeubles atypiques et lumineux destinés à abriter des métiers à tisser imposants mais également les logements des canuts, fleurissent sur la colline de la Croix-Rousse. Parallèlement à ce développement immobilier, la Croix-Rousse se dote d’un réseau de voirie. C’est à ce moment-là qu’apparaissent les ruelles étroites que l’on peut encore observer aujourd’hui, mais aussi les traboules. Ces passages (devenus célèbres) qui traversent les cours des immeubles étaient destinés aux piétons et leur permettaient de gagner plus facilement la presqu’île lyonnaise. L’implantation de l’industrie de la soie vaudra rapidement à la Croix-Rousse le surnom de « colline qui travaille » en opposition avec Fourvière, « la colline qui prie ». Forte de son activité industrielle intensive, Lyon accéda par ailleurs rapidement au statut de première ville ouvrière de France.
    </p>
