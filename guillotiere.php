        <article>
            <h2>Un brin d'histoire</h2>
            <p>La Guillotière est située sur la rive gauche du Rhône et recouvre une partie des 3ème et 7ème arrondissements de Lyon. L'origine du nom est incertaine, venant soit du nom de fameux aubergistes locaux locaux, soit des druides qui en ramenaient du gui. Le faubourg de la Guillotière était contesté par le Dauphiné et n'a été rattaché à la ville de Lyon qu'en 1852 !</p>
        </article>

        <article>
            <h2>La vie culturelle</h2>
            <p>De par sa longue tradition d'accueil et d'arrivée de populations immigrées, "la Guill" est un véritable melting pot ! En effet, de nombreuses associations et amicales de cultures étrangères s'y retrouvent, de l'Asie à l'Afrique en passant par l'Europe de l'est. Les habitants s'impliquent souvent dans la vie locale, que ce soit pour l'aménagement du quartier ou pour organiser des événements festifs qui rayonnent bien au-delà de ses frontières !</p>
        </article>

        <article>
            <h2>Où sortir le soir ?</h2>
            <p>La Guillotière est aussi très connue et appréciée pour sa vie nocturne ! Que ce soit pour un afterwork posé ou une soirée endiablée, de nombreuses options s'offrent à vous. A nouveau, l'identité du quartier se ressentira dans les produits proposés : souvent locaux, équitables et bio ! Mais il ne faut pas négliger ses restaurants qui auront de quoi ravir tous les palets avec des cuisines venues des 4 coins du monde.</p>
        </article>

        <article>
            <h2>Que faire la journée ?</h2>
            <p>Il existe de nombreux lieux à découvrir et surtout d'espaces à explorer à la Guillotière. Le centre Berthelot est un lieu chargé d'histoire, tandis que le cinéma Comoedia propose une sélection de films de qualité. Pour les amoureux d'espaces verts, le parc Sergent Blandan vous ravira avec ses sections tout récemment aménagées, tandis que les quais du Rhône offriront un panorama grandiose avec vue sur Fourvière en fin de journée !</p>
